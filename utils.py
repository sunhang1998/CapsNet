def train_one_epoch(model, optimizer, data_loader, device, epoch):
    model.train()
    loss_function = torch.nn.CrossEntropyLoss()
    accu_loss = torch.zeros(1).to(device)  # 累计损失
    accu_num = torch.zeros(1).to(device)   # 累计预测正确的样本数
    optimizer.zero_grad()
    
    training_loss = 0.0
    sample_num = 0
    length = len(data_loader.dataset)
    data_loader = tqdm(data_loader, file=sys.stdout)
    for step, data in enumerate(data_loader):
        images, labels = data
        labels1 = torch.zeros(labels.size(0), 2).scatter_(1, labels.view(-1, 1), 1.)
        sample_num += images.shape[0]
#         pred = model(images.to(device))
        labels1_pred , images_recon = model(images.to(device),labels1.to(device))
        pred_classes = torch.max(labels1_pred, dim=1)[1]
        accu_num += torch.eq(pred_classes, labels.to(device)).sum()
        
#         loss = loss_function(labels1_pred, labels.to(device))
#         loss.backward()
#         accu_loss += loss.detach()
        loss = caps_loss(labels1.to(device), labels1_pred, images.to(device), images_recon, 0.0005 * 784)
        loss.backward()
        training_loss += loss.item() * images.to(device).size(0)  # record the batch loss
        accu_loss += loss.detach()

        data_loader.desc = "[train epoch {}] loss: {:.3f}, acc: {:.3f}".format(epoch,
                                                                               training_loss / length,
#                                                                                accu_loss.item() / (step + 1),
                                                                               accu_num.item() / length)

        if not torch.isfinite(loss):
            print('WARNING: non-finite loss, ending training ', loss)
            sys.exit(1)

        optimizer.step()
        optimizer.zero_grad()

    return training_loss / length, accu_num.item() / length


